public class Satchel {
    private String color;
    private int vmes;

    public Satchel(String color, int vmes) {
        this.color = color;
        this.vmes = vmes;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getVmes() {
        return vmes;
    }

    public void setVmes(int vmes) {
        this.vmes = vmes;
    }
}



public class Equipment {
    private String name;
    private int wes;

    public Equipment(String name, int wes) {
        this.name = name;
        this.wes = wes;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWes() {
        return wes;
    }

    public void setWes(int wes) {
        this.wes = wes;
    }
}
